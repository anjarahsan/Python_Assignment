# Task 2: Personalized Greeting Program

# Step 1: Take user's first name and last name as input
first_name = input("Enter your first name: ")
last_name = input("Enter your last name: ")

# Step 2: Concatenate to create full name
full_name = first_name + " " + last_name

# Step 3: Print personalized greeting
print("Hello, " + full_name + "! Welcome to the Python world.")
